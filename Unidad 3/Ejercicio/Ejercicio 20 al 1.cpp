/*
3	Hacer un programa para mostrar por pantalla los números del 20 al 1 en orden decreciente.
Importante: El programa no tiene ningún ingreso de datos.


*/

#include <iostream>


using namespace std ;

int i ;

int main() {

	for (i = 20 ; i > 0  ; i-- ) {
        cout << i << endl ; }

	return 0 ;
 }
